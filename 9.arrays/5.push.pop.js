var numbers = [1,2,3]

/*
  ? push  - Adding new elements at the end of an array
*/

numbers.push(4)
numbers.push(5)
numbers.push(6)

/*
  ? unshift  - Adding new elements at the begining of an array
*/
numbers.unshift(0)
numbers.unshift(-1)
numbers.unshift(-2)


/*
  ? pop  - remove the elements at the end of an array
*/
numbers.pop()
numbers.pop()
numbers.pop()

/*
  ? shift  - Remove the elements at the begining of an array
*/

numbers.shift()
numbers.shift()
numbers.shift()
console.log(numbers)